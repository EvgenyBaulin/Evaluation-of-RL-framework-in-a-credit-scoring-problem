def _build_env(
    config: dict[str, Any],
    scenarios: dict[str, Any],
    seed: int,
    control_mode_name: str,
    mode: str,
    scenario_name: str | None = None,
) -> ThresholdControlEnv:
    if mode == "train":
        scale_factor = config["environment"]["train_scale"]
        scenario_pool = list(config["scenarios"]["training_scenarios"])
        fixed_scenario = None if config["environment"]["scenario_randomization"] else scenario_pool[0]
    elif mode == "validation":
        scale_factor = config["environment"]["validation_scale"]
        scenario_pool = [scenario_name or config["scenarios"]["evaluation_scenarios"][0]]
        fixed_scenario = scenario_pool[0]
    else:
        scale_factor = config["environment"]["test_scale"] * config["environment"]["test_subset_fraction"]
        scenario_pool = [scenario_name or config["scenarios"]["evaluation_scenarios"][0]]
        fixed_scenario = scenario_pool[0]
    return ThresholdControlEnv(
        config=config,
        scenarios=scenarios,
        mode=mode,
        scale_factor=scale_factor,
        seed=seed,
        control_mode=control_mode_name,
        scenario_name=fixed_scenario,
        scenario_pool=scenario_pool,
    )


