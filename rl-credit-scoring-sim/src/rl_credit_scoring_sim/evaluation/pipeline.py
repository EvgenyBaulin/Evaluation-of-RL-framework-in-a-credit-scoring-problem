def execute_pipeline(config: dict[str, Any], scenarios: dict[str, Any]) -> dict[str, Any]:
    paths = ensure_directories(config)
    set_global_seed(config["seeds"][0])

    _write_reference_summary(paths["reference_summary"])

    trained_agents, training_logs = _train_agents(config, scenarios, paths)
    if not config["execution"]["run_evaluation"]:
        if not training_logs.empty:
            training_logs.to_csv(paths["logs_dir"] / "training_logs.csv", index=False)
        return {
            "config": config,
            "scenarios": scenarios,
            "paths": paths,
            "training_logs": training_logs,
            "summary_df": pd.DataFrame(),
            "weekly_df": pd.DataFrame(),
            "tables": {},
            "figures": {},
        }

    main_summary, main_weekly = _evaluate_main(config, scenarios, trained_agents)
    ablation_summary = pd.DataFrame()
    ablation_weekly = pd.DataFrame()
    if config["execution"]["run_ablation"]:
        ablation_summary, ablation_weekly = _run_ablations(config, scenarios, paths)

    summary_df = pd.concat([main_summary, ablation_summary], ignore_index=True)
    weekly_df = pd.concat([main_weekly, ablation_weekly], ignore_index=True)

    if not training_logs.empty:
        training_logs.to_csv(paths["logs_dir"] / "training_logs.csv", index=False)
    summary_df.to_csv(paths["logs_dir"] / "summary_runs.csv", index=False)
    weekly_df.to_csv(paths["logs_dir"] / "weekly_runs.csv", index=False)

    tables = _build_tables(config, summary_df, weekly_df, paths)
    lwgb_table = _locally_worse_globally_better_table(tables["weekly_profit_curves"], config, paths)
    tables["locally_worse_globally_better"] = lwgb_table
    figures = {}
    if config["execution"]["run_plotting"]:
        figures = _build_figures(config, tables, weekly_df, paths)
    if config["execution"]["export_writer_handoff"]:
        _write_writer_handoff(config, scenarios, tables, figures, paths)
    return {
        "config": config,
        "scenarios": scenarios,
        "paths": paths,
        "training_logs": training_logs,
        "summary_df": summary_df,
        "weekly_df": weekly_df,
        "tables": tables,
        "figures": figures,
    }


def run_pipeline(project_root: str | Path, profile: str | None = None) -> None:
    project_root = Path(project_root)
    config = load_run_config(project_root, profile=profile)
    scenarios = load_scenarios(project_root)
    execute_pipeline(config=config, scenarios=scenarios)
