def _state_definition_summary(config: dict[str, Any]) -> tuple[str, str]:
    state_dim = int(config["state_dim"])
    features = features_for_dimension(state_dim)
    baseline_names = ", ".join(f"`{feature.name}`" for feature in features[:12])
    state_line = (
        f"State definition: {state_dim}-dimensional observation. "
        f"The first 12 ordered features remain the baseline features: {baseline_names}."
    )
    if state_dim == 12:
        addition_line = "No extra feature blocks were appended in this run; it is the unchanged 12D baseline."
    else:
        addition_names = ", ".join(f"`{feature.name}`" for feature in features[12:])
        addition_line = (
            f"Added beyond the baseline for this run: {addition_names}. "
            "The cumulative layer definitions are recorded in `state_dimension_manifest.md`."
        )
    return state_line, addition_line


def _run_episode(controller, controller_name: str, controller_type: str, env: ThresholdControlEnv, seed: int, run_id: int):
    if hasattr(controller, "begin_episode"):
        controller.begin_episode()
    observation, _ = env.reset(seed=seed)
    done = False
    while not done:
        if controller_type == "baseline":
            decision = controller.predict(observation, env)
            action = decision.action
        else:
            action = controller.predict(observation, deterministic=True)
        observation, _, terminated, truncated, _ = env.step(action)
        done = terminated or truncated
    weekly_records = pd.DataFrame(env.simulator.episode_dataframe(controller_name=controller_name, seed=seed, run_id=run_id))
    if not weekly_records.empty:
        weekly_records = weekly_records.sort_values("interactive_week").reset_index(drop=True)
        weekly_records["cumulative_reward"] = weekly_records["reward"].cumsum()
        weekly_records["cumulative_profit"] = weekly_records["realized_profit"].cumsum()
        weekly_records["cumulative_npv"] = weekly_records["realized_npv"].cumsum()
        weekly_records["controller_type"] = controller_type
    summary = env.simulator.episode_summary()
    summary.update(
        {
            "controller": controller_name,
            "controller_type": controller_type,
            "scenario_name": env.simulator.current_scenario_name,
            "seed": seed,
            "run_id": run_id,
        }
    )
    return summary, weekly_records


def _train_agents(config: dict[str, Any], scenarios: dict[str, Any], paths: dict[str, Path]) -> tuple[dict[tuple[int, str], Any], pd.DataFrame]:
    trained = {}
    training_logs: list[dict[str, Any]] = []
    for seed in config["seeds"]:
        for agent_name in config["controllers"]["agents"]:
            control_mode_name = controller_mode(agent_name)
            env = _build_env(config, scenarios, seed=seed, control_mode_name=control_mode_name, mode="train")
            obs, _ = env.reset(seed=seed)
            action_dim = env.action_space.n if control_mode_name == "discrete" else 0
            controller = make_agent(agent_name, config, action_dim=action_dim, obs_dim=obs.shape[0])
            if config["execution"]["run_training"]:
                controller.fit(
                    env=env,
                    training_episodes=config["training_episodes"],
                    seed=seed,
                    checkpoint_dir=paths["checkpoints_dir"],
                    checkpoint_frequency=config["logging"]["checkpoint_frequency_episodes"],
                    logging_frequency=config["logging"]["logging_frequency_episodes"],
                )
            controller.save(paths["checkpoints_dir"] / f"{agent_name}_seed_{seed}_final")
            validation_env = _build_env(
                config=config,
                scenarios=scenarios,
                seed=seed + 900000,
                control_mode_name=control_mode_name,
                mode="validation",
                scenario_name="base_market",
            )
            validation_summary, _ = _run_episode(
                controller,
                controller_name=agent_name,
                controller_type="agent_validation",
                env=validation_env,
                seed=seed + 900000,
                run_id=-1,
            )
            training_logs.append(
                {
                    "agent_name": agent_name,
                    "seed": seed,
                    "episode": "validation",
                    "cumulative_reward": validation_summary["cumulative_reward"],
                    "scenario_name": validation_summary["scenario_name"],
                    "expected_profit": validation_summary["expected_profit"],
                    "default_rate": validation_summary["default_rate"],
                    "approval_rate": validation_summary["approval_rate"],
                    "log_type": "validation",
                }
            )
            trained[(seed, agent_name)] = controller
            training_logs.extend(getattr(controller, "training_logs", []))
    return trained, pd.DataFrame(training_logs)


def _evaluate_main(config: dict[str, Any], scenarios: dict[str, Any], trained_agents: dict[tuple[int, str], Any]) -> tuple[pd.DataFrame, pd.DataFrame]:
    summary_rows = []
    weekly_frames = []
    evaluation_runs = config["evaluation_runs"]
    for seed in config["seeds"]:
        for agent_name in config["controllers"]["agents"]:
            controller = trained_agents[(seed, agent_name)]
            for scenario_idx, scenario_name in enumerate(config["scenarios"]["evaluation_scenarios"]):
                for run_id in range(evaluation_runs):
                    eval_seed = seed * 1000 + scenario_idx * 100 + run_id
                    env = _build_env(
                        config=config,
                        scenarios=scenarios,
                        seed=eval_seed,
                        control_mode_name=controller_mode(agent_name),
                        mode="test",
                        scenario_name=scenario_name,
                    )
                    summary, weekly = _run_episode(controller, agent_name, "agent", env, eval_seed, run_id)
                    summary["experiment_group"] = "main"
                    summary_rows.append(summary)
                    if not weekly.empty:
                        weekly["experiment_group"] = "main"
                        weekly_frames.append(weekly)

        for baseline_name in config["controllers"]["baselines"]:
            for scenario_idx, scenario_name in enumerate(config["scenarios"]["evaluation_scenarios"]):
                for run_id in range(evaluation_runs):
                    eval_seed = seed * 1000 + 50000 + scenario_idx * 100 + run_id
                    baseline = make_baseline(baseline_name, config)
                    env = _build_env(
                        config=config,
                        scenarios=scenarios,
                        seed=eval_seed,
                        control_mode_name="discrete",
                        mode="test",
                        scenario_name=scenario_name,
                    )
                    summary, weekly = _run_episode(baseline, baseline_name, "baseline", env, eval_seed, run_id)
                    summary["experiment_group"] = "main"
                    summary_rows.append(summary)
                    if not weekly.empty:
                        weekly["experiment_group"] = "main"
                        weekly_frames.append(weekly)

    weekly_df = pd.concat(weekly_frames, ignore_index=True) if weekly_frames else pd.DataFrame()
    return pd.DataFrame(summary_rows), weekly_df


def _ablation_variants(config: dict[str, Any]) -> dict[str, dict[str, Any]]:
    variants = {}

    cfg = deepcopy(config)
    cfg["environment"]["delayed_reward"] = False
    variants["no_delayed_reward"] = cfg

    cfg = deepcopy(config)
    for penalty_name in cfg["reward"]["penalties"]:
        cfg["reward"]["penalties"][penalty_name]["enabled"] = False
        cfg["reward"]["penalties"][penalty_name]["weight"] = 0.0
    variants["no_constraint_penalties"] = cfg

    cfg = deepcopy(config)
    cfg["policy"]["split_policy"] = False
    variants["no_split_policies"] = cfg

    cfg = deepcopy(config)
    cfg["environment"]["risk_aware_shaping"] = False
    variants["no_risk_aware_shaping"] = cfg

    cfg = deepcopy(config)
    cfg["environment"]["scenario_randomization"] = False
    cfg["scenarios"]["training_scenarios"] = ["base_market"]
    variants["no_robustness_logic"] = cfg

    return variants


def _run_ablations(config: dict[str, Any], scenarios: dict[str, Any], paths: dict[str, Path]) -> tuple[pd.DataFrame, pd.DataFrame]:
    primary_agent = config["controllers"]["ablation_primary_agent"]
    summary_rows = []
    weekly_frames = []
    for variant_name, variant_config in _ablation_variants(config).items():
        for seed in config["seeds"]:
            control_mode_name = controller_mode(primary_agent)
            train_env = _build_env(variant_config, scenarios, seed=seed, control_mode_name=control_mode_name, mode="train")
            obs, _ = train_env.reset(seed=seed)
            action_dim = train_env.action_space.n if control_mode_name == "discrete" else 0
            controller = make_agent(primary_agent, variant_config, action_dim=action_dim, obs_dim=obs.shape[0])
            controller.fit(
                env=train_env,
                training_episodes=variant_config["training_episodes"],
                seed=seed,
                checkpoint_dir=paths["checkpoints_dir"],
                checkpoint_frequency=variant_config["logging"]["checkpoint_frequency_episodes"],
                logging_frequency=variant_config["logging"]["logging_frequency_episodes"],
            )
            controller.save(paths["checkpoints_dir"] / f"{primary_agent}_{variant_name}_seed_{seed}_final")
            for scenario_idx, scenario_name in enumerate(variant_config["scenarios"]["evaluation_scenarios"]):
                for run_id in range(variant_config["evaluation_runs"]):
                    eval_seed = seed * 2000 + scenario_idx * 100 + run_id
                    env = _build_env(
                        variant_config,
                        scenarios,
                        seed=eval_seed,
                        control_mode_name=control_mode_name,
                        mode="test",
                        scenario_name=scenario_name,
                    )
                    summary, weekly = _run_episode(
                        controller,
                        f"{primary_agent}__{variant_name}",
                        "agent_ablation",
                        env,
                        eval_seed,
                        run_id,
                    )
                    summary["experiment_group"] = "ablation"
                    summary["ablation_name"] = variant_name
                    summary_rows.append(summary)
                    if not weekly.empty:
                        weekly["experiment_group"] = "ablation"
                        weekly["ablation_name"] = variant_name
                        weekly_frames.append(weekly)
    weekly_df = pd.concat(weekly_frames, ignore_index=True) if weekly_frames else pd.DataFrame()
    return pd.DataFrame(summary_rows), weekly_df


def _ci_settings(config: dict[str, Any]) -> tuple[float, int]:
    ci_cfg = config["confidence_intervals"]
    n_resamples = ci_cfg["bootstrap_resamples"] if ci_cfg["full_ci"] else ci_cfg["fast_bootstrap_resamples"]
    return ci_cfg["confidence_level"], n_resamples


def _add_seed_stability(summary: pd.DataFrame, group_cols: list[str]) -> pd.DataFrame:
    seed_means = (
        summary.groupby(group_cols + ["seed"], as_index=False)[["expected_profit", "cumulative_reward"]]
        .mean()
        .rename(
            columns={
                "expected_profit": "expected_profit_seed_mean",
                "cumulative_reward": "cumulative_reward_seed_mean",
            }
        )
    )
    stability = seed_means.groupby(group_cols, as_index=False).agg(
        expected_profit_seed_std=("expected_profit_seed_mean", "std"),
        cumulative_reward_seed_std=("cumulative_reward_seed_mean", "std"),
    )
    return stability.fillna(0.0)


def _build_tables(config: dict[str, Any], summary_df: pd.DataFrame, weekly_df: pd.DataFrame, paths: dict[str, Path]) -> dict[str, pd.DataFrame]:
    confidence_level, n_resamples = _ci_settings(config)
    main_summary = summary_df[summary_df["experiment_group"] == "main"].copy()
    main_weekly = weekly_df[weekly_df["experiment_group"] == "main"].copy()

    scenario_summary = summarize_with_ci(
        frame=main_summary,
        group_cols=["controller", "controller_type", "scenario_name"],
        metric_cols=METRIC_COLUMNS,
        confidence_level=confidence_level,
        n_resamples=n_resamples,
        seed=123,
    )
    scenario_summary = scenario_summary.merge(
        _add_seed_stability(main_summary, ["controller", "controller_type", "scenario_name"]),
        on=["controller", "controller_type", "scenario_name"],
        how="left",
    )

    overall_summary = summarize_with_ci(
        frame=main_summary,
        group_cols=["controller", "controller_type"],
        metric_cols=METRIC_COLUMNS,
        confidence_level=confidence_level,
        n_resamples=n_resamples,
        seed=321,
    )
    overall_summary = overall_summary.merge(
        _add_seed_stability(main_summary, ["controller", "controller_type"]),
        on=["controller", "controller_type"],
        how="left",
    )

    weekly_reward_curves = summarize_curve_with_ci(
        frame=main_weekly,
        group_cols=["controller", "scenario_name"],
        x_col="interactive_week",
        y_col="cumulative_reward",
        confidence_level=confidence_level,
        n_resamples=n_resamples,
        seed=222,
    )
    weekly_profit_curves = summarize_curve_with_ci(
        frame=main_weekly,
        group_cols=["controller", "scenario_name"],
        x_col="interactive_week",
        y_col="cumulative_profit",
        confidence_level=confidence_level,
        n_resamples=n_resamples,
        seed=333,
    )

    ablation_summary = pd.DataFrame()
    if (summary_df["experiment_group"] == "ablation").any():
        ablation_source = summary_df[summary_df["experiment_group"] == "ablation"].copy()
        ablation_summary = summarize_with_ci(
            frame=ablation_source,
            group_cols=["controller", "scenario_name", "ablation_name"],
            metric_cols=METRIC_COLUMNS,
            confidence_level=confidence_level,
            n_resamples=n_resamples,
            seed=444,
        )

    tables = {
        "main_scenario_summary": scenario_summary,
        "main_overall_summary": overall_summary,
        "weekly_reward_curves": weekly_reward_curves,
        "weekly_profit_curves": weekly_profit_curves,
        "run_level_metrics": summary_df,
        "weekly_run_metrics": weekly_df,
    }
    if not ablation_summary.empty:
        tables["ablation_summary"] = ablation_summary

    if config["execution"]["export_tables"]:
        for name, frame in tables.items():
            frame.to_csv(paths["tables_dir"] / f"{name}.csv", index=False)
    return tables


def _locally_worse_globally_better_table(
    weekly_profit_curves: pd.DataFrame,
    config: dict[str, Any],
    paths: dict[str, Path],
) -> pd.DataFrame:
    baseline = weekly_profit_curves[weekly_profit_curves["controller"] == "static_threshold"]
    rl_controllers = [name for name in config["controllers"]["agents"]]
    rows = []
    pivot_frames = []
    early_cutoff = max(1, int(config["environment"]["horizon_weeks"] * 0.3) - 1)
    for controller in rl_controllers:
        controller_df = weekly_profit_curves[weekly_profit_curves["controller"] == controller]
        for scenario_name in controller_df["scenario_name"].drop_duplicates():
            merged = controller_df[controller_df["scenario_name"] == scenario_name].merge(
                baseline[baseline["scenario_name"] == scenario_name],
                on=["scenario_name", "interactive_week"],
                suffixes=("_controller", "_baseline"),
            )
            if merged.empty:
                continue
            early_row = merged[merged["interactive_week"] == early_cutoff]
            early_gap = 0.0 if early_row.empty else float(
                early_row["cumulative_profit_mean_controller"].iloc[0]
                - early_row["cumulative_profit_mean_baseline"].iloc[0]
            )
            final_gap = float(
                merged["cumulative_profit_mean_controller"].iloc[-1]
                - merged["cumulative_profit_mean_baseline"].iloc[-1]
            )
            rows.append(
                {
                    "controller": controller,
                    "scenario_name": scenario_name,
                    "early_gap": early_gap,
                    "final_gap": final_gap,
                    "locally_worse_globally_better": early_gap < 0 and final_gap > 0,
                }
            )
            if early_gap < 0 and final_gap > 0:
                pivot_frames.append(
                    merged.rename(
                        columns={
                            "cumulative_profit_mean_controller": "cumulative_profit_mean",
                            "cumulative_profit_ci_lower_controller": "cumulative_profit_ci_lower",
                            "cumulative_profit_ci_upper_controller": "cumulative_profit_ci_upper",
                        }
                    )[
                        [
                            "interactive_week",
                            "scenario_name",
                            "cumulative_profit_mean",
                            "cumulative_profit_ci_lower",
                            "cumulative_profit_ci_upper",
                        ]
                    ].assign(controller=controller)
                )
                pivot_frames.append(
                    merged.rename(
                        columns={
                            "cumulative_profit_mean_baseline": "cumulative_profit_mean",
                            "cumulative_profit_ci_lower_baseline": "cumulative_profit_ci_lower",
                            "cumulative_profit_ci_upper_baseline": "cumulative_profit_ci_upper",
                        }
                    )[
                        [
                            "interactive_week",
                            "scenario_name",
                            "cumulative_profit_mean",
                            "cumulative_profit_ci_lower",
                            "cumulative_profit_ci_upper",
                        ]
                    ].assign(controller="static_threshold")
                )
                break
    lwgb_table = pd.DataFrame(rows)
    lwgb_table.to_csv(paths["tables_dir"] / "locally_worse_globally_better.csv", index=False)
    if pivot_frames:
        curve_df = pd.concat(pivot_frames, ignore_index=True)
        first_row = lwgb_table[lwgb_table["locally_worse_globally_better"]].iloc[0]
        plot_locally_worse_globally_better(
            curve_df[curve_df["scenario_name"] == first_row["scenario_name"]],
            paths["figures_dir"] / "locally_worse_globally_better.png",
            title=f"Locally Worse, Globally Better: {first_row['controller']} in {first_row['scenario_name']}",
        )
    return lwgb_table


